// / : 실제 나누기 연산 결과
// % : 나누고 난 몫 이후 나머지
console.log(1993+210);
console.log(1993-210);
console.log(1993*210);
console.log(1993/210);
console.log(1993%210);

// == : 동등연산자, 값만 비교(자동으로 형변환해서 같다고 인식)
// === : 일치연산자, 값과 자료형 비교
let val1 = 1993;
let val2 = '1993';
console.log(val1==val2);
console.log(val1===val2);